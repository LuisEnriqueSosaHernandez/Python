def sumar(n1,n2):
    suma=n1+n2
    return suma
def restar(n1,n2):
    resta=n1-n2
    return resta
def multiplicar(n1,n2):
    mult=n1*n2
    return mult
def dividir(n1,n2):
    if(n2!=0):
        divi=n1/n2
        return divi
    else:
        return 0
def resto(n1,n2):
    if(n2!=0):
        rest=n1%n2
        return rest
    else:
        return 0
